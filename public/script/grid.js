var css = { start: "start", finish: "finish", wall: "wall", active: "active" };

function GraphSearch($graph, options, implementation) {
    this.$graph = $graph;
    this.search = implementation;
    this.opts = $.extend({wallFrequency:0.1, debug:true, gridSize:10}, options);
    this.initialize();
}

GraphSearch.prototype.setOption = function(opt) {
    this.opts = $.extend(this.opts, opt);
    this.drawDebugInfo();
};

GraphSearch.prototype.initialize = function() {
    let $background = $('#main');
    this.grid = [];
    let self = this,
        nodes = [],
        $graph = this.$graph;

    $graph.empty();

    let cellAmount = $background.width() / 16;
    let cellWidth = $background.width() / cellAmount,
        cellHeight = $background.width() / cellAmount;

    let $cellTemplate = $("<span />").addClass("grid_item").width(cellWidth).height(cellHeight),
        startSet = false;

    for(var x = 0; x < this.opts.gridSize; x++) {
        var $row = $("<div class='clear' />"),
            nodeRow = [],
            gridRow = [];

        for (var y = 0; y < this.opts.gridSize; y++) {
            var id = "cell_" + x + "_" + y,
                $cell = $cellTemplate.clone();

            $cell.attr("id", id).attr("x", x).attr("y", y);
            $row.append($cell);
            gridRow.push($cell);

            var isWall = (impassable[x][y] == 1);
            Math.floor(Math.random()*(1/self.opts.wallFrequency));
            if(isWall === true) {
                nodeRow.push(WALL);
                $cell.addClass(css.wall);
            }
            else  {
                var cell_weight = ($("#generateWeights").prop("checked") ? (Math.floor(Math.random() * 3)) * 2 + 1 : 1);
                nodeRow.push(cell_weight);
                $cell.addClass('weight' + cell_weight);
                if ($("#displayWeights").prop("checked")) {
                    $cell.html(cell_weight);
                }
                if (!startSet) {
                    $cell.addClass(css.start);
                    startSet = true;
                }
            }
        }
        $graph.append($row);

        this.grid.push(gridRow);
        nodes.push(nodeRow);
    }

    this.graph = new Graph(nodes);

    // bind cell event, set start/wall positions
    this.$cells = $graph.find(".grid_item");
    this.$cells.mousedown(function(event) {
        switch (event.which) {
            case 1:
                // Left button pressed.
                console.log("grid_item ", $(this).attr('x'), $(this).attr('y'));
                break;
            case 2:
                // Middle Mouse button pressed.
                // toggle grid on and off.
                break;
            case 3:
                let paths = [];
                let moves = [];
                // Find intersecting grid_items with units with class unitSelected.
                const units = document.querySelectorAll('.unitSelected');

                // Unselect css start.
                gridItems = document.querySelectorAll('.start');
                gridItems.forEach(gridItem => {
                    gridItem.classList.remove('start');
                });
                units.forEach(currentUnit => {
                    $("#" + currentUnit.id).isMoving = true;

                    let unitX = $(currentUnit).offset().left / 16;
                    let unitY =  $(currentUnit).offset().top / 16;
                    let gridItem = document.getElementById('cell_' + unitX + '_' + unitY);
                    if (gridItem !== null) {
                        gridItem.classList.add('start');
                    }

                    let path = self.nodeToNode(self.nodeFromUnit(currentUnit), $(this));

                    moves.push({
                        'path' : path,
                        'currentUnit' : currentUnit});
                    paths.push(path);
                });

                for (let moveX = 0; moveX < moves.length; moveX++) {
                    const move = moves[moveX];
                    moveUnit(move.path, move.currentUnit);
                }

                // Don't animate paths if display option isn't on.
                if (self.opts.display === true) {
                    for (let index = 0; index < paths.length; index++) {
                        const path = paths[index];
                        self.animatePath(path);
                    }
                }
                break;
            default:
                console.log('Unknown mouse button', event.which);
        }
    });
};


